let aprovados = ['Bia', 'Carlos', 'Ana']

// Array é acessado através do indice [0], zero é o primeiro indice.
console.log(aprovados[0])
console.log(aprovados[1])
console.log(aprovados[2])

/// adicionar um elemento em um indice especifico.
aprovados[3] = 'Paulo'

console.log(aprovados[3])

// adicionar um elemento no próximo indice disponivel

aprovados.push('José')

console.log(aprovados[4])
